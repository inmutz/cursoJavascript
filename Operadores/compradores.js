//Operadores de comparadores (Relacionales)
let a = 5; 
let b = '5'; 

a == b; 
console.log(a);
console.log(b); 

//Operadores igualadores 
console.log('a == b ->', a==b);


//Operado de Igual estricto o exacto (tipo y dato)
console.log('a === b ->', a === b);

//String interpolation 
console.log(`${a} === ${b}`, a === b );

//Operador de distinto 
//Compara valor y convertir en tipo de dato si es necesario 

console.log(`${a} != ${b}`, a != b); 

//Operador de distinto exacto 
console.log(`${a} !== ${b}`, a !== b); 

//Operador menor 
console.log(`${a} < ${b}`, a < b ); 

//Operador menor o igual 
console.log(`${a} <= ${b}`, a <= b );

// Operadro mayor 
console.log(`${a} > ${b}`, a > b); 

//Operadro de mayor o igual 
console.log(`${a} >= ${b}`, a >= b)
